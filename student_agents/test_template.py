import template as t
def test_evaluate():
    agent = t.Agent()  # Replace with the name of your agent class

    # Test case 1: Empty board
    board = [['--' for _ in range(8)] for _ in range(8)]
    assert agent.evaluate(board) == 0

    # Test case 2: Board with pieces
    board = [['--', '--', '--', '--', '--', '--', '--', '--'],
             ['--', '--', '--', '--', '--', '--', '--', '--'],
             ['--', '--', '--', '--', '--', '--', '--', '--'],
             ['--', '--', '--', 'wp', '--', '--', '--', '--'],
             ['--', '--', '--', '--', '--', '--', '--', '--'],
             ['--', '--', '--', '--', '--', '--', '--', '--'],
             ['--', '--', '--', '--', '--', '--', '--', '--'],
             ['--', '--', '--', '--', '--', '--', '--', '--']]
    assert agent.evaluate(board) == 10  # Assuming getPieceValue('wp') returns 1

    # Test case 3: Board with different pieces
    board = [['--', '--', '--', '--', '--', '--', '--', '--'],
             ['--', '--', '--', '--', '--', '--', '--', '--'],
             ['--', '--', '--', '--', '--', '--', '--', '--'],
             ['--', '--', '--', 'wp', '--', '--', '--', '--'],
             ['--', '--', '--', '--', 'bp', '--', '--', '--'],
             ['--', '--', '--', '--', '--', '--', '--', '--'],
             ['--', '--', '--', '--', '--', '--', '--', '--'],
             ['--', '--', '--', '--', '--', '--', '--', '--']]
    assert agent.evaluate(board) == 0 

